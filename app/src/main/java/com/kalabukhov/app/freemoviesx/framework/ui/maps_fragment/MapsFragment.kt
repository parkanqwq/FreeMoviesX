    override fun onCreateOptionsMenu(menu: Menu, inflater: MenuInflater) {
        super.onCreateOptionsMenu(menu, inflater)
        inflater.inflate(R.menu.map_menu, menu)
        val action_like = menu.findItem(R.id.action_like)
        val menu_content_provider = menu.findItem(R.id.menu_content_provider)
        val action_local_map = menu.findItem(R.id.action_local_map)
    }

    companion object {

        const val BUNDLE_EXTRA_MAP = "map"

        fun newInstance(bundle: Bundle): MapsFragment {
            val fragment = MapsFragment()
            fragment.arguments = bundle
            return fragment
        }
    }
}